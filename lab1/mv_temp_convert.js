let fahrenheit = 75.38; //this is the average for October of last year in Medellin, Colombia, according to TuTiempo.net
let celsius = (fahrenheit - 32) * 5 / 9 
let kelvin = (fahrenheit + 459.67) * 5 / 9

console.log("Temperature(°F): " + fahrenheit)
console.log("Temperature(°C): " + celsius)
console.log("Temperature(K): " + kelvin)

